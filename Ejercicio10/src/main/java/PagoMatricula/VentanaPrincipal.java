    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
      // Codigo del boton calcular:
        double estrato,patrimonio, pago;
        String numInscripcion;
        String nombre;
        patrimonio = Double.parseDouble(txtPatrimonio.getText());
        estrato = Double.parseDouble(txtEstrato.getText());
        numInscripcion = (txtNumeroInscripcion.getText());
        nombre= (txtNombre.getText());      
        
        pago = Calculo.Pago(estrato, patrimonio);
        txtPago.setText(String.valueOf(pago));
        
        nombre = Calculo.Nombre_Estudiante(nombre);
        txtName.setText(String.valueOf(nombre));
        
        numInscripcion = Calculo.Numero_Inscripcion(numInscripcion);
        txtInscripcion.setText(String.valueOf(numInscripcion));
    }//GEN-LAST:event_btnCalcularActionPerformed

