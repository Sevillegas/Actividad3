
package RaizCuadradoCubo;


public class Calculo {
 public static double Calcular_Cuadrado (
    double a)
    
   {
    double solucion;
   
    solucion = Math.pow(a,2);
    return solucion;
   }
 
 public static double Calcular_Cubo (
    double a)
    
   {
    double solucion;
   
    solucion = Math.pow(a,3);
    return solucion;
   }
 public static double Calcular_Raiz (
    double a)
    
   {
    double solucion;
   
    solucion = Math.sqrt(a);
    return solucion;}
    
}
