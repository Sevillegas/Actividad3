    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // Codigo del boton calcular btnCalcular:
        double a ,solucion1, solucion2, solucion3;
        
        a = Double.parseDouble(txtNumero.getText());
       
        
        
        solucion1 = Calculo.Calcular_Raiz(a);
        txtRaiz.setText(String.valueOf(solucion1));
        solucion2 = Calculo.Calcular_Cuadrado(a);
        txtCuadrado.setText(String.valueOf(solucion2));
        solucion3 = Calculo.Calcular_Cubo(a);
        txtCubo.setText(String.valueOf(solucion3));
    }//GEN-LAST:event_btnCalcularActionPerformed

