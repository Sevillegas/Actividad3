    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // codigo del boton calcular:
        double a ,b, mayor,igual;
           
        
        a = Double.parseDouble(txtA.getText());
        b = Double.parseDouble(txtB.getText());
         
        
        mayor = Calculo.Calcular_Mayor(a, b);
        txtMayor.setText(String.valueOf(mayor));
        
        igual = Calculo.Calcular_Iguales(a,b);
        txtIguales.setText(String.valueOf(igual));
        txtIgual.setText(String.valueOf(igual));
    }//GEN-LAST:event_btnCalcularActionPerformed

