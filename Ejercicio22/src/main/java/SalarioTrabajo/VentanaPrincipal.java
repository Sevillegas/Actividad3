    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        // codigo del boton calcular btnCalcular
           double horasTrabajadas ,salarioHora, salarioMensual;
           String nombre;
        
        horasTrabajadas = Double.parseDouble(txtHorasTrabajadas.getText());
        salarioHora = Double.parseDouble(txtSalarioHora.getText());
        nombre = (txtNombre.getText());
        
        salarioMensual = Salario.Calcular_Salario(horasTrabajadas,salarioHora);
        txtSalarioMensual.setText(String.valueOf(salarioMensual));
        
        nombre = Salario.Nombre_Empleado(nombre);
        txtName.setText(String.valueOf(nombre));

    }//GEN-LAST:event_btnCalcularActionPerformed

